public class CalculateClass {

    public CalculateClass() {
    }
    
    public float sum(float LHS,float RHS)
    {
        return LHS+RHS;
    }
    public int sumIntegers(int LHS,int RHS)
    {
        return LHS+RHS;
    }
    public int sumSubtrCT(int LHS,int RHS)
    {
        return LHS-RHS;
    }
}
